#include <stdio.h>
#include <unistd.h>

void	ft_putchar(char str)
{
	write (1, &str, 1);
}
int	main(int argc, char *argv[])
{
	int	i;

	i = 0;
	while (argv[0][i] != '\0' && argc)
	{
		ft_putchar(argv[0][i]);
		++i;
	}
	ft_putchar('\n');
	return (0);
}

#include <unistd.h>

void	ft_print_numbers(void)
{
	char	*a;
	int	i;
	
	i = 0;
	a = "01234568789";
	while (a[i])
	{
		write (1, &a[i], 1);
		i++;
	}
}

int	main(void)
{
	ft_print_numbers();
}
